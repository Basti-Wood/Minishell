#include "../../include/minishell.h"

void	free_redirs(t_redir *redirs)
{
	t_redir	*tmp;

	while (redirs)
	{
		tmp = redirs->next;
		if (redirs->filename)
			free(redirs->filename);
		free(redirs);
		redirs = tmp;
	}
}
